# maximo y minimo

minimo = min(34,2,5)
print(minimo)

lista = [32,43,56]
print(max(lista))

# en strings

lista = ['ana', 'belen', 'tati']
menor = min(lista)
print(menor)

# diccionarios
dic = {'c':45, 'c2':23}
print(min(dic))
# valores del dic
dic = {'c':45, 'c2':23}
print(min(dic.values()))
