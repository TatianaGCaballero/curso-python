x = 5
y = 7

# suma
print(f'la suma de {x} y {y} es igaul a {x + y}')
# resta
print(f'la resta de {x} y {y} es igaul a {x - y}')
# mukti
print(f'la multi de {x} y {y} es igaul a {x * y}')
# division
print(f'la divi de {x} y {y} es igaul a {x / y}')
# divi al piso
print(f'la divi al piso de {x} y {y} es igaul a {x // y}')
# modulo
print(f'el modulo de {x} y {y} es igaul a {x % y}')
# elelvaion
print(f'la elevacion de {x} y {y} es igaul a {x ** y}')
# raiz
print(f'la raiz de {x} y {y} es igaul a {x **0.5}')